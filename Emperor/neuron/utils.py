class NeuronCluster(Module):
    def __init__(
        self,
        *,
        neuron: Neuron,
        start_router: RouterDecisionSquash,
        output_generator: FeedForward,
        total_rows: int,
        total_cols: int,
        num_signals: int,
        num_signal_split: int,
        mem_size: int = 128,
    ):
        super().__init__()

        self.total_rows = total_rows
        self.total_cols = total_cols
        self.num_signals = num_signals
        self.num_signal_split = num_signal_split

        # FEATURE EXTRACTOR
        self.feature_extractor_1 = nn.Conv2d(
            in_channels=3, out_channels=num_signals, kernel_size=3, padding=1, stride=2
        )
        self.feature_extractor_2 = nn.Conv2d(
            in_channels=num_signals,
            out_channels=num_signals,
            kernel_size=3,
            padding=1,
            stride=2,
        )
        self.feature_extractor_activation = nn.GELU()

        # FEATURE EXTRACTOR AND OUTPUT GENERATOR
        for i in range(num_signals):
            setattr(self, f"start_router{i}", copy.deepcopy(start_router))
            getattr(self, f"start_router{i}").terminal_connections(
                0, (i + total_cols // 2 - num_signals // 2)
            )
