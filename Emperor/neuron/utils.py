class NucleusProcessingGenerator(Module):
    def __init__(
        self,
        *,
        d_input: int,
        input_processing: FeedForward,
        params_generator: FeedForward,
    ):
        super().__init__()
        self.d_input = d_input
        self.input_processing = input_processing
        self.params_generator = params_generator

    def forward(self, input):
        """
        Input
            `input` [self.mem_size, self.d_input] -> Memory read together with the input
        Return
            `memory` [self.mem_size, self.d_input] -> Processed read memory
        """

        """
        - generate wieghts with `param_generator` using inputs
        - process inputs with `input_processing` using again the inputs
        - then i do a linear processed_input * generated_weights + generated_biases
        the result of this is [mem_size, d_input]
        - flatten the output generated above and put it through a output_processing
        then return the flattened
        """

        # Flattened input of shape [1, self.mem_size * self.d_input]
        params_input = input.reshape(1, -1)
        # Generate all parameters into a vector of shape [1, self.d_output * self.d_output + self.mem_size]
        params = self.params_generator(params_input)

        # Weights of shape [self.d_input, self.d_input]
        weights = params[:, : self.d_input**2].reshape(self.d_input, self.d_input)
        # Bias of shape [1, self.d_input]
        bias = params[:, self.d_input**2 :].T

        # Processed input [self.mem_size, self.d_input]
        processed = self.input_processing(input)

        # Process input with generated weights with a shape of [self.mem_size, self.d_input]
        memory = torch.mm(processed, weights) + bias

        return memory


class NucleusProcessingMHA(Module):
    def __init__(self, *, d_input: int, mha_heads: int):
        super().__init__()
        self.multihead_attn = nn.MultiheadAttention(d_input, mha_heads)

    def forward(self, input):
        """
        Input
            `input` [self.mem_size, self.d_input] -> Memory read together with the input
        Return
            `output` [1, self.d_input] -> Processed vector based on the neuron input and read memory
            `memory` [self.mem_size, self.d_input] -> Processed read memory
        """
        memory, _ = self.multihead_attn(query=input, key=input, value=input)
        return memory


class NucleusProcessingTranspose(Module):
    def __init__(
        self,
        *,
        d_input: int,
        mem_size: int,
        input_processing: FeedForward,
        transpose_processing: FeedForward,
        output_processing: FeedForward,
    ):
        super().__init__()
        self.d_input = d_input
        self.mem_size = mem_size

        self.input_processing = input_processing
        self.input_processing_norm = nn.LayerNorm(d_input)

        self.transpose_processing = transpose_processing
        self.transpose_processing_norm = nn.LayerNorm(mem_size)

        self.output_processing = output_processing

    def forward(self, input):
        """
        Input
            `input` [self.mem_size, self.d_input] -> Memory read together with the input
        Return
            `output` [1, self.d_input] -> Processed vector based on the neuron input and read memory
            `memory` [self.mem_size, self.d_input] -> Processed read memory
        """
        memory = self.input_processing(input)
        memory_transposed = self.transpose_processing(memory.T)

        memory = self.input_processing_norm(memory)
        memory_transposed = self.transpose_processing_norm(memory_transposed).T

        memory = self.output_processing((memory + memory_transposed))
        return memory


