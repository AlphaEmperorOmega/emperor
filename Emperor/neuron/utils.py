class RouterGeneratorSquash(Module):
    """
    Router that uses `FeedForwardSquash` to generate `[total_axons, d_input]` vectors
    the one with the highest length will be sent to one out of 4 groups of terminals where
    again `FeedForwardSquash` will be used to generated `[total_terminals, d_input]` vectors
    and the one with the highest length will be send further.

    Process:
    - use `FeedForwardSquash` to generate `[total_axons, d_input]` vectors for each axon
    - store the length of each vector in `[total_axons, d_input]` into a vector `[total_axons, 1]`
    - store the maximum value and its index of the vector `[total_axons, 1]`
    - check i the maximum value is higher than the treshold if not stop and return the largest
    vector in `[total_axons, d_input]`
    - if the treshold is passed use `FeedForwardSquash` to generate `[total_terminals, d_input]`
    vecotrs for each terminal
    - store the length of each vector in `[total_terminals, d_input]` into a vector `[total_terminals, 1]`
    - store the top `n_split` maximum values and it's indexes of the vector `[total_terminals, 1]`
    - for each `n_split` signal
        - check if the length is high enough otherwise skip the current signal
        - store the row and column of the current signal
        - check if the signal is in the alowed rand of columns or rows
        - if all checks are passed you store the row, column and the vectors that will be passed further
    - in the end of no signals were selected stop and send further only the vectors with the highest length
    """

    def __init__(
        self,
        *,
        axon_router: FeedForwardGeneratorSquash,
        terminal_router: FeedForwardGeneratorSquash,
        row_range: int,
        col_range: int,
        total_rows: int,
        total_cols: int,
        num_split_signals: int,
    ):
        """
        `d_input`: input dimension
        `d_ff`: hidden state dimmension
        `row_idx`: row index of the current neuron
        `col_idx`: column index of the current neuron
        `row_range`: row range the current euron has access to
        `col_range`: column range the current neuron has access to
        `total_rows`: total number of neurons allowed on a row
        `total_cols`: total number of neurons allowed on a column
        `n_split`: number of signals that can be selected from a group of terminals
        """
        super().__init__()
        self.num_split_signals = torch.tensor(num_split_signals)
        self.row_range = row_range
        self.col_range = col_range
        self.total_rows = total_rows
        self.total_cols = total_cols
        self.total_axons = 4
        self.terminals_per_axon = col_range * 2 + 1
        self.total_col_range = col_range * 2 + 1

        self.threshold = 0.8

        # Used to predict the axon to which data will be sent
        self.axon_ff = axon_router

        # Used to predict to which terminal data will be sent
        for i in range(self.total_axons):
            setattr(self, f"terminal_ff_{i}", terminal_router)

    def forward(self, input: Tensor):
        """
        `x`: Shape(1, self.d_input) -> distribution generated by the model
        `prev_neuron_length`: type scalar -> the length of the last terminal
        """
        # Based on the input calculate a vector for each axon
        axon_vectors = self.axon_ff(input)
        # Store the length of each generated vector for each axon
        axon_lengths = torch.norm(axon_vectors, dim=-1).unsqueeze(0)
        # Store the highest length and its idx for the selected axon
        axon_length, axon_idx = torch.max(axon_lengths, dim=-1)
        # Check if the vector length is high enough
        if axon_length < self.threshold:
            # Return the vector with the largest length that does not pass the threshold
            output = axon_vectors[
                axon_idx
            ]  # + (1 - axon_length) * torch.randn_like(input)
            # axon_prob * input + (1 - axon_prob) * torch.randn_like(input)
            # terminal_probs[0] * axon + (1 - terminal_probs[0]) * torch.randn_like(axon)
            return [output], False, False

        # Pass through terminal the layer linked with the selected axon
        terminal_vectors = getattr(self, f"terminal_ff_{axon_idx.item()}")(
            axon_vectors[axon_idx]
        )
        # Store the length of each generated vector for each terminal
        terminal_lengths = torch.norm(terminal_vectors, dim=-1)
        # Store the num_split_signals highest length and its idx of the selected terminals
        terminal_lengths, terminal_idxs = torch.topk(
            terminal_lengths, self.num_split_signals
        )
        outputs, rows, cols = [], [], []
        # Loop over the selected terminals
        for terminal_length, terminal_idx in zip(terminal_lengths, terminal_idxs):
            # Check is the current length is high enough to be sent to the next neuron
            if terminal_length < self.threshold:
                continue
            # Get the coordinates of the current neurons
            row, col = self.terminal_connections[axon_idx][terminal_idx]
            # Check if the selected neuron is accessible
            if row < 1 or row > self.total_rows or col < 1 or col > self.total_cols:
                continue
            else:
                rows.append(row)
                cols.append(col)
                terminal_idx = terminal_idx.unsqueeze(0)
                outputs.append(terminal_vectors[terminal_idx])
        # If there are no selected neurons stop and send only the signal with the vector with the highest length further
        if len(outputs) == 0:
            terminal_idx = terminal_idxs[0].unsqueeze(0)
            output = terminal_vectors[
                terminal_idx
            ]  # * (1 - terminal_lengths[0]) * torch.randn_like(input)
            return [output], False, False

        self.axons_count[axon_idx.item()] += 1
        self.terminals_count[axon_idx.item()][terminal_idxs[0].item()] += 1

        return outputs, rows, cols

    def terminal_connections(self, row_idx, col_idx):
        # Range of all layers the current neuron has access to, when
        # row_range = 4 and row_idx = 0
        # Ex: [-1, 0, 1, 2, 3]
        # Negative numbers indicate connections do not exist and positive otherwise
        row_range_indexes = torch.arange((row_idx - 1), (row_idx + self.row_range - 1))

        # Range of all neuron connections the current neuron has access to when
        # col_range = 3 and col_idx = 0
        # Ex: [-3, -2, -1,  0,  1,  2,  3]
        # Negative numbers indicate connections do not exist and positive otherwise
        col_range_indexes = torch.arange(
            (col_idx - self.col_range), (col_idx + self.col_range + 1)
        )

        # Get all coordinates accessible by the current neuron, accessible or not
        # [[-1, -3], [-1, -2], [-1, -1], [-1,  0], [-1,  1], [-1,  2], [-1,  3],
        #  [ 0, -3], [ 0, -2], [ 0, -1], [ 0,  0], [ 0,  1], [ 0,  2], [ 0,  3],
        #  [ 1, -3], [ 1, -2], [ 1, -1], [ 1,  0], [ 1,  1], [ 1,  2], [ 1,  3],
        #  [ 2, -3], [ 2, -2], [ 2, -1], [ 2,  0], [ 2,  1], [ 2,  2], [ 2,  3]]
        curr_neuron_connections = torch.cartesian_prod(
            row_range_indexes, col_range_indexes
        )

        # Generate terminal connections for each axon, Connections available for each 4 axons:
        # - axon 1: [[-1, -3], [-1, -2], [-1, -1], [-1,  0], [ 0, -3], [ 0, -2], [ 0, -1]]
        # - axon 2: [[-1,  1], [-1,  2], [-1,  3], [ 0,  0], [ 0,  1], [ 0,  2], [ 0,  3]]
        # - axon 3: [[ 1, -3], [ 1, -2], [ 1, -1], [ 1,  0], [ 2, -3], [ 2, -2], [ 2, -1]]
        # - axon 4: [[ 1,  1], [ 1,  2], [ 1,  3], [ 2,  0], [ 2,  1], [ 2,  2], [ 2,  3]]
