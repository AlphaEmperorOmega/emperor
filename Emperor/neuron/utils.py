class RouterRowColCoordinates(Module):
    """
    ## Router that predicts `num_rows + num_cols` coordinates of neurons based connections of the current neuron.

    Process:
    -
    """

    def __init__(
        self,
        *,
        d_input: int,
        d_ff: int,
        d_output: int,
        row_idx: int,
        col_idx: int,
        row_range: int,
        col_range: int,
        total_rows: int,
        total_cols: int,
    ):
        super().__init__()
        self.row_range = row_range
        # Range of all layers the current neuron has access to, when
        # row_range = 4 and row_idx = 0
        # [-1, 0, 1, 2]
        # Negative numbers indicate layers that do not exist,
        # while positive numbers are layers that exist
        self.row_range_indexes = torch.arange((row_idx - 1), (row_idx + row_range - 1))
        # Range of all neuron connections the current neuron has access to when
        # col_range = 4 and col_idx = 0
        # Ex: [-4, -3, -2, -1,  0,  1,  2,  3,  4]
        # Negative numbers indicate neuron connections that do not exist,
        # while positive numbers indicate neuron connections that exist
        self.col_range_indexes = torch.arange(
            (col_idx - col_range), (col_idx + col_range + 1)
        )
        # Used to predict the embeding that will be concatenated with the input that will
        # Generate the prediction will be separated to predict the row and column
        self.ff = FeedForwardDefault(d_input=d_input, d_ff=d_ff, d_output=d_output)
        # Used to check if all elements of the logits is 1
        self.activation = nn.ReLU()
        # Probability distribution generator
        self.softmax = nn.Softmax(dim=-1)

    def forward(self, x: torch.Tensor):
        # Embedding logits used to predict the desired embedding ids
        logits = self.ff(x)
        if torch.sum(self.activation(logits)) == 0:
            return False, False, False, False
        # The first self.row_range logits will be used to predict the row
        # of the all possible connections but used to predict the embedding index
        row = F.softmax(logits[:, : self.row_range], dim=-1)
        # All logits after the first self.row_range is used to predict the column
        # of the all possible connections but used to predict the embedding index
        col = F.softmax(logits[:, self.row_range :], dim=-1)

        # Check if the row or coulumn exists.
        if row < 0 or row > self.total_rows or col < 0 or col > self.total_cols:
            return False, False, False, False
        # Highest probability and index of the neuron row to predict the embedding index
        row_prob, row_idx = torch.max(row, dim=-1, keepdim=True)
        # Highest probability and index of the neuron column to predict the embedding index
        col_prob, col_idx = torch.max(col, dim=-1, keepdim=True)

        return row_prob, row_idx, col_prob, col_idx


class FeedForwardGeneratorSquash(Module):
    """
    Generate `n_signals` vectors that are between 0 and 1 using `Squash()` that
    are having as input `[1, d_input]` and returning `[n_signals, d_input]` vectors
    """

    def __init__(self, *, d_input: int, num_signals: int, activation=nn.GELU()):
        """
        `d_input`: dimension of the input
        `n_signals`: number of signals to be generated
        `activation`: activation function
        """
        super().__init__()
        self.num_signals = num_signals
        self.activation = activation
        self.ff = nn.Linear(d_input, d_input * num_signals)
        self.squash = Squash()

    def testInput(self, d_input):
        x = torch.arange(d_input).view(1, d_input).float()
        print("# Input: ", x)
        output = self.forward(x)
        print("# Output: ", output)
        print("# Vector lengthts per head: ", torch.norm(output, dim=-1))

    def forward(self, input: Tensor) -> Tensor:
        """
        Inputs:
            `input`: (1, self.d_input)
        Output:
            `output`: (self.n_signals, self.d_input)
        """
        output = self.activation(self.ff(input).reshape(self.num_signals, -1))
        return self.squash(output)


