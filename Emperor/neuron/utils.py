class AddPositionEmbs(Module):
    """
    Adds learnable positional embeddings to the inputs

    Attributes
        posemb_init: Positional embeding initializer

    Return
        Output of shape `[bs, timesteps, in_dim]`
    """

    def __init__(self, *, d_input: int, memory_size: int = 64):
        super().__init__()
        self.pos_embedding = nn.Parameter(torch.empty(memory_size, d_input))
        self.reset_parameters()

    def reset_parameters(self) -> None:
        init.normal_(self.pos_embedding, std=0.02)

    def forward(self, input):
        return input + self.pos_embedding


class MemoryReadWrite(Module):
    def __init__(self, *, d_input: int, ff: FeedForward):
        """
        `ff_type: str = "default_ff"` type of feed forward to use. Options: ["default_ff", "multihead_ff"]
        `d_input: int` input dimension
        `d_ff: int` hidden dimension
        `mem_size: int` number of memory cells. Example: 128
        `read_write_heads: int` number of cells to read or write. Read ex: 5. Write ex: 128
        `layer_heads: List[int] = []` number of heads per hidden multihead layer. Example: [32, 16, 32]
        `layer_gates: Union[bool, List[bool]] = False` boolean values that indicates if a multihead hidden layer will have a gate. Example: [False, True, False]
        `layer_dropout: Union[float, List[float]] = 0.0` float values between 0 and 1 that indicates the dropout for each hidden multihead hidden layer. Example: [0.7, 0.8, 0.5]
        `is_weight_generator: bool = False` dictates if memory weights are generated by the `FeedForward`
        `activation = nn.GELU()` activation function
        """
        super().__init__()
        self.d_input = d_input
        self.d_ff = d_ff

        self.memory_norm = nn.LayerNorm(d_input)
        self.weight_generator = ff

    def forward(self, memory):
        """
        Inputs:
            `input` [1, d_input] -> Input output generated by the previous neuron.
            `memory` [mem_size, d_input] -> All memory cells of a neuron.
        """
        selected = self.memory_norm(memory)
        # Check if the use of the generator

        selected = self.weight_generator(memory)
        # Generate sofmtax probs that selects a specific row form the input
        selected = F.softmax(selected.T / 0.5, dim=-1)
        feat = memory
        # Do a soft selection of the memory cells by multiplying
        # - selected memory cells of shape [self.read_write_heads, self.mem_size]
        # - memory of shape [self.memory_size, d_input]
        # Resulting into the soft selection of specific memory cells
        # Of shape [self.read_write_heads, d_input]
        feat = torch.mm(selected, feat)
        return feat

