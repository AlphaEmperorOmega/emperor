class FeedForward(Module):
    def __init__(
        self,
        *,
        ff: Union[FeedForwardDefault, FeedForwardMultiHead],
        is_vector_norm: bool = False,
        is_squash: bool = False,
        output_activation: Union[None, Callable[[torch.Tensor], torch.Tensor]] = None,
    ):
        """
        `ff: Union[FeedForwardDefault, FeedForwardMultiHead]` type of feed forward to use. Options: ["default_ff", "multihead_ff"]
        `is_vector_norm: bool = False` indicates if input is squashed to a length of 1
        `is_squash: bool = False` indicates if the input is squashed to a length between 0 and 1
        `output_activation: bool = False` indicates if the output before returning is passed through an activation function
        `activation = nn.GELU()` activation function
        """
        super().__init__()
        self.ff = ff
        self.is_vector_norm = is_vector_norm
        self.is_squash = is_squash
        self.output_activation = output_activation

        if is_vector_norm:
            self.squash = VectorNorm()
        if is_squash:
            self.squash = Squash()

    def testInput(self, d_input):
        x = torch.arange(d_input).view(1, d_input).float()
        print("# Input: ", x)
        print("# Input shape:", x.shape)
        output = self.forward(x)
        print("\n# Output: ", output)
        print("# Output shape:", output.shape)
        print("# Vector lengthts per head: ", torch.norm(output, dim=-1))

    def forward(self, input):
        output = self.ff(input)

        if self.output_activation is not None:
            output = self.output_activation(output)

        if self.is_squash or self.is_vector_norm:
            output = self.squash(output)

        return output


